# for loop for string
print("For loop for string:")
name = "madhava"
for i in name:
    print(f"The character of name:",i)

# for loop for tuple
print("For loop for tuple:")
movies = ("pushpa", "kgf", "rrr", "kalki", "bahubali")
for mv_name in movies:
    print(f"The movie {mv_name} is blocker bluster hit")

# for loop in list
print("for loop in list:")
heros = ["prabhas", "ntr", "ram charan", "allu arjun", "nani", "Ram"]
for actor in heros:
    print(f"{actor} was from tollywood")

